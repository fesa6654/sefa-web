<script>
import mailgonder from "./servisler/email";

export default {
  data() {
    return {
      year: new Date().getFullYear(),
      window: {
        width: 0,
        height: 0
      },
      mailinfo: {
        isim: "",
        eposta: "",
        icerik: ""
      },
      sayfa: 0
    };
  },
  mounted() {
    window.sr = new ScrollReveal();
    /*
rotate: {x:30, y:50, z:40}
delay: 1000
*/
    sr.reveal(".profil", {
      duration: 1000,
      scale: 0.5
    });

    sr.reveal(".linkler", {
      duration: 1000,
      distance: "100px",
      origin: "right"
    });

    sr.reveal(".animations", {
      interval: 300,
      distance: "100px",
      origin: "right"
    });

    sr.reveal(".logos", {
      interval: 100,
      distance: "100px",
      origin: "bottom",
      opacity: 0
    });

    sr.reveal(".reveal", {
      duration: 1500,
      distance: "100px",
      origin: "bottom",
      opacity: 0
    });

    sr.reveal(".animasyon", {
      duration: 500,
      interval: 1000,
      distance: "100px",
      origin: "left",
      opacity: 0
    });

    sr.reveal(".animasyon-1", {
      duration: 500,
      interval: 500,
      distance: "100px",
      origin: "right",
      opacity: 0
    });

    sr.reveal(".animat", {
      duration: 500,
      interval: 300,
      distance: "10px",
      origin: "right"
    });
  },
  created() {
    window.addEventListener("resize", this.handleResize);
    this.handleResize();
  },
  destroyed() {
    window.removeEventListener("resize", this.handleResize);
  },
  methods: {
    handleResize() {
      this.window.width = window.innerWidth;
      this.window.height = window.innerHeight;
    },
    mail_gonder() {
      if (
        this.mailinfo.isim != "" &&
        this.mailinfo.eposta != "" &&
        this.mailinfo.icerik != ""
      ) {
        mailgonder
          .addemail(this.mailinfo)
          .then(response => {
            this.eposta();
            this.$vs.notify({
              icon: "email",
              color: "success",
              title:
                "Sayın" +
                " " +
                this.mailinfo.isim +
                " " +
                "E-Mailinizi Aldım. En Kısa Sürede Size Cevap Vereceğim!!",
              position: "top-right",
              time: 10000
            });
          })
          .catch(err => {
            this.$vs.notify({
              icon: "error_outline",
              color: "danger",
              title: "E-Mailiniz Gönderilirken Hata Oluştu!!",
              position: "top-right"
            });
          });
      } else {
        this.$vs.notify({
          icon: "error_outline",
          color: "danger",
          title: "Lütfen Boş Yerleri Doldurunuz!!",
          position: "top-right"
        });
      }
    },
    eposta() {
      mailgonder
        .getemail()
        .then(response => {})
        .catch(err => {});
    }
  }
};
</script>

